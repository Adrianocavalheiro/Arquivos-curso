public class RevisaoJava {
    public static void main(String[] args) {

        // Declarada variavel do tipo string
        String nome = "Adriano Cavalheiro";

        // Declarei uma variavel do TIPO PRIMITIVO INTEIRO
        int idade = 33;

        // Declarei uma variavel do TIPO PRIMITIVO DOUBLE (ACEITA NUMEROS COM VIRGULA)
        double peso = 90.0;

        // Declarei uma variavel do TIPO PRIMITIVO CHAR (caractere unico)
        char sexo = 'M';

        double soma = idade + peso;

        // Concatenando valores na saida de dados
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade + " anos");
        System.out.println("Peso: " + peso + " Kg");
        System.out.println("Sexo: " + sexo);
        System.out.println("Soma da idade e do peso: " + soma);

        System.out.println("-------------------------------------------------------------------------------");

    }

}
